package Entity;

public class Phanca {
private int Maca;
private int MaNV;
public Phanca(int maca, int maNV) {
	super();
	Maca = maca;
	MaNV = maNV;
}
public int getMaca() {
	return Maca;
}
public void setMaca(int maca) {
	Maca = maca;
}
public int getMaNV() {
	return MaNV;
}
public void setMaNV(int maNV) {
	MaNV = maNV;
}

}
